class PrintUtil {
//  是否为测试环境
  static const bool inProduction =
      const bool.fromEnvironment("dart.vm.product");

  static i(Object object) {
    if (!inProduction) {
      print("------------------------------------------------------------------------------------------------------------------------------");
      print(object);
      print("------------------------------------------------------------------------------------------------------------------------------");
    }
  }

  static e(Object object) {
    if (!inProduction) {
      print(object);
    }
  }

  static selfError(Object object) {
    if (!inProduction) {
      print(
          "========================================================================================================================================");
      print(object);
      print(
          "========================================================================================================================================");
    }
  }

  static selfErrorCatch(Object object) {
    if (!inProduction) {
      print(
          "~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
      print(object);
      print(
          "~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
    }
  }
}
